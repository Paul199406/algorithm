// 面试题31：栈的压入、弹出序列
// 题目：输入两个整数序列，第一个序列表示栈的压入顺序，请判断第二个序列是
// 否为该栈的弹出顺序。假设压入栈的所有数字均不相等。例如序列1、2、3、4、
// 5是某栈的压栈序列，序列4、5、3、2、1是该压栈序列对应的一个弹出序列，但
// 4、3、5、1、2就不可能是该压栈序列的弹出序列。

#include <cstdio>
#include <stack>

using namespace std;

#define nullptr 0

bool IsPopOrder(const int* pPush, const int* pPop, int nLength)
{
    if ( nLength==0 || pPush==nullptr || pPop==nullptr )//空数组
    {
       return false;
    }

    stack<int>  stData;
    int pPushPos=0;//pPush数组当前的偏移位置
    int pPopPos =0;//pPop数组当前的偏移位置

    bool bResult = false;
    while( pPopPos < nLength )
    {
        
        while ( (stData.size()!=0) && (stData.top() == pPop[pPopPos])  )//栈顶元素与当前要弹出元素一致
        {
            stData.pop();
            pPopPos++;
        }
    
        if ( (stData.size()==0) && (pPopPos==nLength) )//完成,栈空且出栈数组结束
        {
            bResult = true;
            break;
        }
        else//无论是空栈还是数值不同,当要继续压栈直到找到相同
        {
            if ( pPushPos>=nLength )//无法完成入栈
            {
                bResult =  false;
                break;
            }
           
            while ( pPushPos<nLength )//可以继续压栈
            {
                stData.push(pPush[pPushPos]);
                pPushPos++;
              
                if ( pPop[pPopPos] == stData.top() )//直到相等
                {
                    break;
                }
            }
        }
           
        
    }
    return bResult;
}



bool IsPopOrder1(const int* pPush, const int* pPop, int nLength)
{
    bool bPossible = false;

    if(pPush != nullptr && pPop != nullptr && nLength > 0)
    {
        const int* pNextPush = pPush;
        const int* pNextPop = pPop;

        std::stack<int> stackData;

        while(pNextPop - pPop < nLength)
        {
            // 当辅助栈的栈顶元素不是要弹出的元素
            // 先压入一些数字入栈
            while(stackData.empty() || stackData.top() != *pNextPop)
            {
                // 如果所有数字都压入辅助栈了，退出循环
                if(pNextPush - pPush == nLength)
                    break;

                stackData.push(*pNextPush);

                pNextPush ++;
            }

            if(stackData.top() != *pNextPop)
                break;

            stackData.pop();
            pNextPop ++;
        }

        if(stackData.empty() && pNextPop - pPop == nLength)
            bPossible = true;
    }

    return bPossible;
}

